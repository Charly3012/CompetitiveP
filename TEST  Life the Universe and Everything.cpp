#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int v;

    while (true)
    {

        cin >> v;

        if (v != 42)
        {
            cout << v << "\n";
        }
        else
        {
            break;
        }
    }

    return 0;
}